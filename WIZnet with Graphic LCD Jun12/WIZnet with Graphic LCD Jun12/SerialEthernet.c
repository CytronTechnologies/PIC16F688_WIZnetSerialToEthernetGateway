/***** Sub function *****/
void pic_init(void)
{
    // Set internal Oscillator 8MHz
    IRCF2 = 1;
    IRCF1 = 1;
    IRCF0 = 1;

    ANSEL = 0;  // Set all digital pin

    // Off comparator
    CM2 = 1;
    CM1 = 1;
    CM0 = 1;

    // Clear all ports
    PORTA = 0b010000;
    PORTC = 0b000000;

    // Port direction
    TRISA = 0b000000;
    TRISC = 0b100000;

    // Enable UART function
    BRG16 = 0;
    SYNC = 0;
    TX9 = 0;
    RX9 = 0;
    BRGH = 1;       // Select high speed baud rate
    SPBRG = 51;     // Configure the baud rate (9600)
    SPEN = 1;       // Enable serial port
    CREN = 1;       // Enable reception
    TXEN = 1;       // Enable transmission
}

void glcd_init(void)
{
    GLCD_RST = 1;
    delay_ms(15);
    glcd_write(0x21);	// Function set
    glcd_write(0xC0);	// Set Vlcd
    glcd_write(0x13);	// Set Bias System
    glcd_write(0x20);	// Function set
    glcd_write(0x0C);	// Sets display configuration
}

void glcd_write(unsigned char c)
{
    int i;
    GLCD_CS = 0;
    for(i=0;i<=7;i++)
    {
        GLCD_SDA = (c>>(7-i))&1;
        GLCD_SCLK = 0;
        GLCD_SCLK = 1;
    }
    GLCD_CS = 1;
}

void glcd_goto(char row, char col)
{
    GLCD_DC = 0;
    glcd_write(0x80+col-1);
    glcd_write(0x40+row-1);
    GLCD_DC = 1;
}

void glcd_putchar(unsigned char c)
{
    int i;
    if((c<32)|(c>127)) c = ' ';
    for(i=0;i<5;i++) glcd_write(FontLookup[c-32][i]);
    glcd_write(0x00);
}

void glcd_putstr(const char *s)
{
    GLCD_DC = 1;
    while(*s) glcd_putchar(*s++);
}

void glcd_number(unsigned int no, char base, char digit)
{
    char i,j,di[10];
    for(i=0;i<10;i++) di[i] = 0;
    i = 0;

    do
    {
	di[i] = no % base;
	no = no / base;
	i = i + 1;
    }
    while(no!=0);

    for(j=digit;j>0;j--)
    {
	if(di[j-1]<=9) glcd_putchar(di[j-1]+'0');
	else glcd_putchar(di[j-1]-10+'A');
    }
}

void uart_transmit(unsigned char c)
{
    while(!TXIF);
    TXREG = c;
}

void uart_putstr(const char *s)
{
    while(*s) uart_transmit(*s++);
}

unsigned char uart_receive(void)
{
    if(OERR)
    {
        CREN = 0;
        CREN = 1;
    }
    while(!RCIF);
    return RCREG;
}

